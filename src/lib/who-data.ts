export function getWhoReference(
  sex: "male" | "female" | "M" | "F" | string,
  metric: "weight" | "height",
): WhoRow[] {
  const isMale = sex.toLowerCase().startsWith("m");
  if (metric === "weight") return isMale ? WFA_BOYS : WFA_GIRLS;
  return isMale ? HFA_BOYS : HFA_GIRLS;
}

/** Interpolate a WHO reference row for a fractional age in months */
export function interpolateWho(table: WhoRow[], ageMonths: number): WhoRow | null {
  if (ageMonths < 0 || ageMonths > 60) return null;
  for (let i = 0; i < table.length - 1; i++) {
    const lo = table[i];
    const hi = table[i + 1];
    if (ageMonths >= lo[0] && ageMonths <= hi[0]) {
      const t = (ageMonths - lo[0]) / (hi[0] - lo[0]);
      return [
        ageMonths,
        lo[1] + t * (hi[1] - lo[1]),
        lo[2] + t * (hi[2] - lo[2]),
        lo[3] + t * (hi[3] - lo[3]),
        lo[4] + t * (hi[4] - lo[4]),
        lo[5] + t * (hi[5] - lo[5]),
      ];
    }
  }
  return null;
}
